macro_rules! declare_icons {
    (
        $( $name:ident => $value:expr, )*
    ) => {
        #[allow(dead_code)]
        pub enum Icon {
            $( $name, )*
        }

        impl Icon {
            pub fn css_class(&self) -> &'static str {
                match self {
                    $( Icon::$name => $value, )*
                }
            }
        }
    };
}

